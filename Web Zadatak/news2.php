			<h2>Spatial Audio in 'Budget Cuts': A VR Stealth Game</h2>
			<p>The developers behind the VR stealth game 'Budget Cuts' discuss how they used Steam Audio to implement advanced spatial audio features for their title. </p>
			<p>The talk provides specific examples of spatial audio technology and how it enhances gameplay and immersion, including: HRTF-based binaural rendering for locating enemies; occlusion and transmission for modeling how sound is affected by vents or other openings; and real-time physics-based reverb for modeling smooth variations and subtle details in environmental reverb.</p>
			<p> The talk also discusses how the needs of the game informed improvements in how Steam Audio integrates with Budget Cuts' game engine, Unity. Finally, the talk discusses some of the limitations in audio engine architecture when it comes to advanced spatial audio technology, and offers some suggestions for addressing them."</p>
