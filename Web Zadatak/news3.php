			<h2>Kerbal Developers Depart Valve</h2>
			<p>In mid 2017, former Valve employee Roger Lundeen revealed a group of developers from KSP - the team who developed Kerbal Space Program - had joined Valve and were working on a new Valve title.A year on it appears that both Kasper Nahuijsen and Jose Palacios have left Valve. Interestingly, Kasper's Linkedin states he worked on "production and product ownership for an as of yet unannounced multi platform video game." Jose had no further detail from his 23 months at Valve, after leaving this week.
			<p>Both Jose and Kasper were founding members of Seven Headed Studios, alongside their ex-colleague Ted Everett - who appears to still be at Valve. We approximate around 5 other individuals who joined from Kerbal are still at Valve, too. It appears Valve allowed the team to retain ownership of the game they were working on after leaving Valve. It is unclear whether this means Valve are still working on 3 titles internally or now just 2."</p>
